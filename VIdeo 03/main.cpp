#include <iostream>
using namespace std;
int main(){
    int n;
    n = 23; 
    cout << "n = "<< n << endl;
    
    int a = 25;
    // cout << "hello world" << endl;
    cout << "a = " << a << endl;
    float b = 1.25;
    cout << sizeof(b) << endl;
    cout << "b = " << b << endl;
    bool c = false; // in boolean algebra true is 1 and false is 0
    cout << "c = " << c << endl;
    char d = 'M';
    cout << "d = " << d << endl;
    double e = 0.12345;
    cout << "e = " << e;
    return 0;
}